public class Tupla {

    int i;
    int j;

    public Tupla(int i, int j){
        this.i = i;
        this.j = j;
    }

    public void setI(int i){
        this.i = i;
    }

    public void setJ(int j){
        this.j = j;
    }

    public int getI(){
        return i;
    }

    public int getJ(){
        return j;
    }
}
